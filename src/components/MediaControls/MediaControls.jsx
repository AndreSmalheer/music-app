function MediaControls({
  audioPlayerRef,
  onPlay,
  onPause,
  onNext,
  onPrevious,
  isPlaying,
  title,
  artist,
  coverSrc,
}) {
  return (
    <NavLink to="/now-playing" className="media-controls-link">
      <div className="media-controls">
        <div className="media-info-section">
          <img className="media-cover-image" src={coverSrc} alt="" />
          <div className="media-text">
            <p className="media-song-title">{title || "No track"}</p>
            <p className="media-artist-name">{artist || ""}</p>
          </div>
        </div>

        <div className="media-buttons-section">
          <button
            className="media-control-button"
            onClick={(e) => {
              e.preventDefault();
              e.stopPropagation();
              onPrevious();
            }}
            aria-label="Previous"
          >
            <PrevBtn />
          </button>

          <button
            className="media-control-button media-play-button"
            onClick={(e) => {
              e.preventDefault();
              e.stopPropagation();

              if (isPlaying) {
                onPause();
              } else {
                onPlay();
              }
            }}
            aria-label={isPlaying ? "Pause" : "Play"}
          >
            {isPlaying ? <PauseBtn /> : <PlayBtn />}
          </button>

          <button
            className="media-control-button"
            onClick={(e) => {
              e.preventDefault();
              e.stopPropagation();
              onNext();
            }}
            aria-label="Next"
          >
            <NextBtn />
          </button>
        </div>
      </div>
    </NavLink>
  );
}

