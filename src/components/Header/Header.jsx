function Header() {
  const location = useLocation();
  const path = location.pathname.toLowerCase();

  const isBackPage = 
    path.includes("now-playing") || 
    path.includes("see-all") || 
    path.includes("settings") || 
    path.includes("download") || 
    path.includes("playlist") || 
    path.includes("artist") ||
    path.includes("create-playlist");

  const isPlaylistDetail = path.includes("playlist/") && !path.includes("edit-playlist");

  return (
    <div className="Header">
      <AnimatePresence mode="wait">
        {isBackPage ? (
          <BackBtn key="back" />
        ) : (
          <SettingsBtn key="settings" />
        )}
      </AnimatePresence>
      {isPlaylistDetail && <EditBtn />}
    </div>
  );
}

export default Header;
